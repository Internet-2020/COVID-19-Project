    		<h1>疟疾</h1>
    		<p style="font-size: 15px">疟疾是每年导致人类死亡最多的疾病之一，据报导全球每年约有3～5亿人感染疟疾，死亡人数超过100万人，主要分布在非洲等国家。2017年全球共发生2.19亿疟疾病例，43.5万名患者死亡，其中非洲占90%以上。在20世纪40年代，中国估计每年有3000万疟疾病例，30万人死亡。但自2016年8月以来，中国未发生一例本土疟疾病例，主要原因就是有青蒿素等特效药、对病人尤其是出入南亚等疫区人员严格跟踪排查、周围人群集体服药等措施。这在历史上疟疾曾造成巨大损失的国家是一项辉煌的成就。中国科学家屠呦呦等人发明的青蒿素在全球范围内已治疗2亿多人，共挽救620万人的生命。目前世界卫生组织已经提出了2030年时使全球疟疾发病率和死亡率进一步降低90%的目标，如果普及青蒿素（目前撒哈拉以南非洲地区估计仅有13%的发热儿童获得了以青蒿素为基础的联合疗法）治疗，应该还是有很大希望实现的。</p >
