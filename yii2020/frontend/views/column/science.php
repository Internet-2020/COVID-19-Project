    		<h1>鼠疫</h1>
    		<p style="font-size: 15px">鼠疫又称黑死病，一般先在鼠间或其它啮齿类野生动物间流行，借助鼠蚤叮咬人而造成人间鼠疫，未经治疗的鼠疫病死率高达50%～70%。在中国法定报告传染病名录上，甲等传染病只有两种，鼠疫位居第一位。历史上的三次鼠疫大流行分别是在公元前6世纪（520-565年）、14世纪中叶、19世纪末期到20世纪30年代。第一次鼠疫大流行从6世纪中叶开始至8世纪消失，在欧亚夺走上亿条生命。疫情起源于埃及的西奈半岛，542年开始在君士坦丁堡爆发。当时的君士坦丁堡是全世界范围内人口最多的城市，很多来自亚洲、非洲、欧洲的商队、船队在此聚集。因疫情爆发适逢拜占庭皇帝查士丁尼在位，故此次瘟疫被后人称为“查士丁尼瘟疫”。 第二次世界性鼠疫大流行从14世纪20年代开始，到1800年左右才停止，初期几年欧洲就有近三千万人因黑死病而死，前后三百余年间欧洲大陆丧失三分之一到一半的人口。在十五、十六世纪黑死病多次侵袭欧洲，但死亡率及严重程度逐渐下降。中世纪各国原本是宗教势力控制，神职人员也充当医生，当时神职人员将鼠疫当做从地狱来的恶魔，得了鼠疫的人就是被恶魔附身的人，神职人员往往要鞭挞患者伤处，而且神职人员会在驱魔的过程中不断接触鼠疫患者，这就必然导致一个结果——神职人员是高危人群。原本大家都认为神职人员是受上帝保护的，应该不会被“恶魔附身”，但却看到神职人员大量病死，人们突然发现自己信仰的无所不能的上帝抛弃了大家，于是怀疑、否定宗教的思想产生，文艺复兴开始了。后来人们养成了隔离和卫生习惯，使用肥皂，随着黑死病逐渐消失，人们更加相信科学，唯物主义就成为势不可挡的潮流。同时，由于黑死病导致太多人死亡，社会劳动力急剧匮乏，导致农奴制走向衰落，新的资本主义的生产方式成为社会更广泛的追求。第三次鼠疫大流行是1894年突然爆发的，至20世纪30年代达最高峰，总共波及亚洲、欧洲、美洲和非洲的六十多个国家，死亡达千万人以上。此次流行传播速度之快、波及地区之广，远远超过前两次大流行。这次流行的特点是疫区多分布在沿海城市及其附近人口稠密的居民区，家养动物中也有流行。其中印度最严重，死亡900万人。此次疫情多分布在沿海城市及其附近人口稠密的居民区，流行传播速度之快，波及地区之广，远远超过前两次大流行。第三次大流行也传播到中国东北，1910年秋冬传到哈尔滨，这次传播的是肺鼠疫，同腺鼠疫不同，腺鼠疫只能鼠传人，不能人传人，而肺鼠疫可以人传人，媒介是飞沫传播。这也是人类第一次认识肺鼠疫。按照其他国家的致死率，这次传播怎么说也得横尸百万，但实际上并没有，只有6万人死亡，原因就是中国有国士伍连德。当时31岁的伍连德面对极端落后的条件，主要采取了三种措施防治鼠疫：一是隔离，建立医院收治病人，保证病人不与其他人接触，控制疫区人员流动，居民彼此隔离，尽量不要出门；二是做口罩，当时中国还没有口罩，伍连德设计了一种极其简单的双层纱布囊口罩，即用两层纱布，内置一块吸水药棉，戴上它就可以隔离病患，成本费仅需当时国币2分半钱；三是焚烧死者尸体，火葬在中国直到上世纪80年代才普及，尤其农民都要求土葬不接受火葬，在当时压力更是巨大，但伍连德上奏清朝政府，由于清朝统治者对疫情极为恐惧，准许火葬。这三种措施实施下来，只67天，就控制住了鼠疫传播。鼠疫到现在就没有那么恐怖了，因为致病的是鼠疫杆菌，自从人类社会有了抗生素和噬菌体，对细菌类致病的传染病都有了较好的控制效果。</p>
