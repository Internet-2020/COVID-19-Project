    		<h1>结核病</h1>
    		<p style="font-size: 15px">结核病是由结核杆菌感染引起的慢性传染病。结核菌可能侵入人体全身各种器官，但主要侵犯肺脏，所以也常称肺结核病。世界上约有三分之一的人口感染结核杆菌，但其中只有很小一部分人会患上结核病。免疫系统较弱的人患结核病的风险更高。艾滋病毒携带者患活动性结核病的可能性是常人的约26～31倍。上世纪八九十年代，结核病发病数全球每年以10%速度递增，尤其是发展中国家问题严重，由于我国农村公共卫生体系也被破坏，我国结核病反弹也是在这段时期。新世纪以来，结核病发病率有所下降，目前，每年全球有超过百万人死于结核病。2015年，共有180万人死于结核病（包括40万艾滋病毒携带者）；2017年，1000万人患有结核病，160万人因该病死亡；2018年，全球结核病死亡数约124万。结核病位居全世界前10位死因之列，排位在艾滋病和疟疾之前。世界共有1040万例结核病新发病人，其中10%为艾滋病毒感染者，前七名国家占到总负担的64%，印度首当其冲，其次是印度尼西亚、中国、菲律宾、巴基斯坦、尼日利亚和南非，主要发生于非洲、亚洲、拉丁美洲等地区。古代结核病是不治之症，被称为“肺痨”，得上最后都会咳血而亡。现在结核病也是可防可治的，有了抗生素等药物，但在经济落后、卫生条件差的地区，难以获得医疗资源的穷人中仍然广泛传播。</p>
