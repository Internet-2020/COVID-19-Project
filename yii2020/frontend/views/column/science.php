    		<h1>霍乱</h1>
    		<p style="font-size: 15px">这是甲等传染病的第二种疾病。霍乱是一种由霍乱弧菌引起的烈性肠道传染病，它通常通过被霍乱弧菌感染的水和食物传播。感染霍乱的人主要表现为剧烈泻吐并排泄大量米泔水样肠内容物，并会出现脱水、肌肉痉挛、少尿和无尿的症状。较严重的则会因休克、尿毒症或者酸中毒而死亡。19世纪初至20世纪末，大规模流行的世界性霍乱共发生8次，地区性流行也出现过几次。1817～1823年，霍乱第一次大规模流行，从“人类霍乱的故乡”印度恒河三角洲蔓延到欧洲，仅1818年前后便使英国6万余人丧生。印度一直有水葬的习俗，人死后尸体在恒河顺流而下。1817 年恒河洪水泛滥，尸体携带的霍乱在恒河下游地区迅速流行开来，后来波及整个印度大陆，又传播到曼谷、泰国和菲律宾等地，1821 年传入我国东南沿海，造成霍乱在亚洲地区的大流行。本次疫情于1824年基本结束。1826～1837年，霍乱第二次大流行，穿越俄罗斯并先后到达德国、英国、加拿大和美国。1846～1863年霍乱第三次大流行，波及整个北半球。1865～1875年，霍乱第四次大流行，由一艘从埃及到英国的船所引发。1883～1896年，霍乱第五次大流行，到达了埃及。1910～1926年，第六次霍乱大流行。1961年出现第七次霍乱大流行，始于印度尼西亚，波及五大洲140多个国家和地区，报告患者逾350万。首先起于印度尼西亚，然后传到亚洲其他国家和欧洲，1970年它又进入非洲，已经百年不见霍乱踪影的非洲从此深受其害。1992年10月，第八次霍乱大流行，席卷印度和孟加拉国部分地区，短短2～3个月就报告病例10余万，死亡人数达几千人。1991年，霍乱开始袭击拉丁美洲，1年之内就造成40万人感染，4000人死亡。到了1993年，仍有78个国家报告存在霍乱，在霍乱流行的高峰时期，每年全球都会有20万人死于霍乱。1997年9月起，霍乱在非洲大规模蔓延，仅1998年的头3个月乌干达就报告病例11335例，肯尼亚报告病例10108例。这次据世界卫生组织统计，2001年非洲霍乱患者占了全球的94%，至今，一些非洲国家，霍乱仍未得到有效的控制。控制霍乱疫情，最简单的办法就是控制上水与下水，上水就是自来水，水源干净、食物清洁，同时收集下水，污水不随便乱排放，基本就能控制霍乱的传播。加上静脉输液、口服补液盐等措施，霍乱的致死率已经非常低。例如1932年中国霍乱大流行，源头是上海，当时全国24省近10万人发病，死亡3万多。又是时伍连德坐镇中央霍乱局，采取的措施非常简单，紧急铺设6条自来水管道，动用水车送水，解决了30万苏州河沿岸居民清洁饮水问题，最终300万人口的大上海在这次霍乱中只死了300余人。建国后主要是开展爱国卫生运动，其中“三管一灭”（管水、管粪、管饮食、灭苍蝇）对消灭霍乱起了重要作用，很快就将控制了霍乱的影响。</p >
