    		<h1>流感</h1>
  			<p style="font-size: 15px">从近百年致死人数上看，流感要比疟疾更厉害，只不过平常大家习以为常，都不关注了。导致几次流感的病毒都来自于鸟类和家禽。1889～1890年俄罗斯流感，在飞机尚未成为交通工具的背景下，仅用几个月就传遍全球。受流感影响最大的群体是15-40岁之间的人群，其次是老人。初期流感的死亡率高达5%～8.75%，总共导致100万人死亡。20世纪以来，流感先后三次大流行，最严重的就是1918年的流感。1918年3月4日位于美国堪萨斯州的军营发生流感，接着中国、西班牙、英国等除澳洲以外的各大洲都发生了流感。由于这次流感是从西班牙开始报道的，所以又称西班牙流感，但实际上跟西班牙没什么关系，源头是美国的养殖场。初期流感症状只有头痛、高烧、肌肉酸痛和食欲不振而已，但到了1918年秋季则在全球大量爆发，至1920年春季，在全世界造成约10亿人感染，大约死亡5000万～1亿人，当时全球才17亿人，这场流感被认为是“最大的医学大屠杀历史”。因为这场大流感，导致参战各国都没有额外的兵力作战，这场流感的易感人群还是年轻人，比俄罗斯流感更集中，主要是20-35岁的青壮年，可以想见，已经死亡了千万人，然后战场上还缺人，战场上肯定没法隔离，一训练彼此之间传播更快，去参战不死于战火也会死于流感，所以兵员枯竭，最终在死亡的阴影下，第一次世界大战仓促落幕。1957～1958年亚洲流感由H2N2亚型流感病毒引起，1957年2月下旬在中国贵州初次发现，1957年夏天蔓延到南半球，1957年6月传至美国，最终导致200万人死亡。1968～1969年香港流感，由H3N2亚型流感病毒引起，1968年初此病毒在香港初次发现，最终导致200万人死亡。美国约有3.38万人死于此次流感大流行。自1920年以来的100年来，流感所致死亡的总和可能已经超过了1918年大流感所致的死亡人数。流感作为一种急性呼吸道传染病，并不仅仅是一场普通的感冒。流感病毒直接侵袭人体的呼吸系统，可引起病毒性肺炎、继发细菌性肺炎、急性呼吸窘迫综合征、休克、弥漫性血管内凝血等多种威胁生命的严重并发症。世界卫生组织估计，全球范围内，每年流感季节性流行可导致65万例死亡，相当于每48秒就有1人因流感死亡。现在，每年全球因流感的死亡人数在百万以上。下呼吸道感染是目前全球感染疾病死亡的主要原因。比如，2016年下呼吸道感染导致250万人死亡。2019年9月，美国爆发了大规模的甲乙混合型流感，截至目前，美国至少有2200万人感染，死亡人数超过1.2万人。</p >
